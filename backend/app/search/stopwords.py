"""
日本語ストップワード（助詞・助動詞など、検索に不要な語）
"""

# 日本語の基本的なストップワード（助詞・助動詞・接続詞など）
JAPANESE_STOPWORDS = {
    # 助詞
    "が", "の", "を", "に", "へ", "と", "から", "より", "で", "や", "か", "も",
    "は", "ば", "て", "つ", "し", "た", "だ", "ね", "よ", "わ", "な", "ん",
    "さ", "ぞ", "ぜ", "や", "やら", "け", "けれども",
    
    # 助動詞
    "だ", "です", "である", "ます", "ません", "ました", "でした",
    "ない", "なかった", "られる", "せる", "させる", "れる",
    
    # 接続詞
    "が", "けれども", "しかし", "そして", "また", "それで", "だから", "ので",
    "そこで", "すると", "ところが", "でも", "でば", "ならば",
    
    # 代名詞・指示語
    "これ", "それ", "あれ", "どれ", "この", "その", "あの", "どの",
    "ここ", "そこ", "あそこ", "どこ",
    
    # 疑問詞（内容を問う重要な疑問詞は除外）
    "か", "かい", "かな", "かしら",
    
    # その他頻出語（意味が薄い）
    "こと", "もの", "ため", "よう", "など", "とき", "時", "際",
    "する", "なる", "ある", "いる", "できる", "される",
}


def remove_stopwords(tokens: list[str]) -> list[str]:
    """
    トークンリストからストップワードを除去する
    
    Args:
        tokens: トークンのリスト
        
    Returns:
        ストップワードを除去したトークンリスト
    """
    return [token for token in tokens if token.lower() not in JAPANESE_STOPWORDS]


def is_stopword(word: str) -> bool:
    """
    単語がストップワードかどうかを判定する
    
    Args:
        word: 判定する単語
        
    Returns:
        ストップワードならTrue
    """
    return word.lower() in JAPANESE_STOPWORDS
